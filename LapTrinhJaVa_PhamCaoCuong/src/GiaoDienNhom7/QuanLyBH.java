    public  void DocFile(){
        try {
            //Bước 1: Tạo đối tượng luồng và liên kết nguồn dữ liệu
            File f = new File("XeLoaiXe.txt");
            BufferedReader br;
            //Bước 2: Đọc dữ liệu
            try (FileReader fr = new FileReader(f)) {
                //Bước 2: Đọc dữ liệu
                br = new BufferedReader(fr);
                String line;
                XE.clear();
                while ((line = br.readLine()) != null) {
                    String[] str = line.split(";");
                    Xe Xe2 = new Xe();
                    LoaiXe lx = new LoaiXe();
                    Xe2.setMaXe(str[0]);
                    Xe2.setTenXe(str[1]);
                    Xe2.setDungTich(str[2]);
                    lx.setTenLoai(str[3]);
                    lx.setXuatXu(str[4]);
                    Xe2.setDonGia(Integer.parseInt(str[5]));
                    lx.setMaLoai(str[6]);
                    Xe2.setLOAIXE(lx);
                    XE.add(Xe2);
                }
                //Bước 3: Đóng luồng
            }
            br.close();
        } catch (IOException | NumberFormatException ex) {
            System.out.println("Loi doc file: " + ex);
        }
    }
    
    public void GhiFile()
    {
        try {
            //Bước 1: Tạo đối tượng luồng và liên kết nguồn dữ liệu
            File f = new File("XeLoaiXe1.txt");
            FileWriter fw = new FileWriter(f);

            //Bước 2: Ghi dữ liệu
            for (int i=0;i<XE.size();i++) {
                fw.write(XE.get(i).toString());// ghi vào file
            }
            //Bước 3: Đóng luồng
            fw.close();
        } catch (IOException ex) {
            System.out.println("Loi ghi file: " + ex);
        }
    }
    
