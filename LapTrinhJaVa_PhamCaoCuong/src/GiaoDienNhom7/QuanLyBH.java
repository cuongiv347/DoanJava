    public Xe TimXe()
    {
        Xe xe = new Xe();
        for(int i = 0; i < XE.size(); i++)
        {
            if(jComboBox1.getSelectedItem().toString() == null ? XE.get(i).getTenXe() == null : jComboBox1.getSelectedItem().toString().equals(XE.get(i).getTenXe()))
            {
                return XE.get(i);
            }
        }
        return xe;
    }
    public CTDonDatHang KhoiTao()
    {
        DonDatHang dh = new DonDatHang();
        CTDonDatHang cT = new CTDonDatHang();
        cT.setSoLuong(Integer.parseInt(txtSoLuong.getText()));
        cT.setXE(TimXe());
        dh.setMaDDH(cT.getXE().getMaXe());
        Date date = new Date();
        SimpleDateFormat ft = new SimpleDateFormat ("dd/MM/yyyy");
        dh.setNgayLap( ft.format(date));
        
        cT.setDONDATHANG(dh);
        return cT;
    }
