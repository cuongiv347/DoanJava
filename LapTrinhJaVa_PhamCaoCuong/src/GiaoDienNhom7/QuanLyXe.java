    private void ClearField() {
        txtMaXe.setText(null);
        txtTenXe.setText(null);
        txtLoaiXe.setText(null);
        txtMau.setText(null);
        txtGia.setText(null);
        txtNgayNhap.setText(null);
        txtHangXe.setText(null);
        txtMaXe.requestFocus();
    }

    public void Show() {
        XeHoi xeHoi = new XeHoi();
        xeHoi = ds.getList().get(ds.getList().size() - 1);
        model.addRow(new Object[]{xeHoi.getnMaXe(), xeHoi.getnTenXe(), xeHoi.getnLoaiXe(), xeHoi.getnMau(), xeHoi.getnGia(), xeHoi.getnNgayNhap(), xeHoi.getnHangXe()});
    }

    private void ShowAll() {
        for (XeHoi xeHoi : ds.getList()) {
            model.addRow(new Object[]{xeHoi.getnMaXe(), xeHoi.getnTenXe(), xeHoi.getnLoaiXe(), xeHoi.getnMau(), xeHoi.getnGia(), xeHoi.getnNgayNhap(), xeHoi.getnHangXe()});
        }
    }

