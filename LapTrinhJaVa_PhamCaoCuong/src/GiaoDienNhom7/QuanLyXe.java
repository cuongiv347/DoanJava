    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        try {
            if (txtMaXe.getText().length() == 0 || txtTenXe.getText().length() == 0 || txtLoaiXe.getText().length() == 0 || txtMau.getText().length() == 0 || txtGia.getText().length() == 0 || txtNgayNhap.getText().length() == 0 || txtHangXe.getText().length() == 0) {
                JOptionPane.showConfirmDialog(rootPane, "Không được để trống!!!", "Thông báo", JOptionPane.ERROR_MESSAGE);
            } else {
                // kiem tra ma cong trinh
                boolean flagMaXe = true;
                for (XeHoi xeHoi : ds.getList()) {
                    if (txtMaXe.getText().equals(xeHoi.getnMaXe())) {
                        flagMaXe = false;
                        break;
                    }
                }
                if (flagMaXe) {
                    XeHoi xeHoi = new XeHoi();
                    xeHoi.setnMaXe(txtMaXe.getText());
                    xeHoi.setnTenXe(txtTenXe.getText());
                    xeHoi.setnLoaiXe(txtLoaiXe.getText());
                    xeHoi.setnMau(txtMau.getText());
                    xeHoi.setnGia(txtGia.getText());
                    xeHoi.setnNgayNhap(txtNgayNhap.getText());
                    xeHoi.setnHangXe(txtHangXe.getText());
                    ds.insert(xeHoi);
                    Show();
                    ClearField();
                } else {
                    JOptionPane.showConfirmDialog(rootPane, "Mã xe đã tồn tại!!!", "Thông báo", JOptionPane.ERROR_MESSAGE);
                    txtMaXe.requestFocus(true);
                }
            }
        } catch (Exception ex) {
            JOptionPane.showConfirmDialog(rootPane, "Dữ liệu không hợp lệ!!!", "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnThemActionPerformed

